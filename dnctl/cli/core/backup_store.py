"""Backup file store on a remote SFTP host (``dnftp``).

Backups land on the same external SFTP server that ``ts_store`` uses for
tech-support tarballs — :data:`BACKUP_HOST`. Devices SFTP to ``dnftp`` with
the fixed account below into :data:`BACKUP_DIR` (the cli-mcp namespace
under ``/ftpdisk/dn/oshaboo/cli/backups``). The MCP, regardless of which
machine it runs on, verifies / lists / deletes those files by opening its
own SFTP session to the same host with the same credentials. Net effect:
the same code base works unchanged on any MCP host that can reach ``dnftp``
in the lab — backups don't depend on the local filesystem the way they
historically did.

Layout
------

The on-dnftp tree is **always rooted at the device alias**::

    <BACKUP_DIR>/<device>/                              # device root
    ├── <device>__<UTC>__<desc>.md                      # bucket=None
    └── <bucket>/                                       # optional sub-bucket
        └── <device>__<UTC>__<desc>.md

- ``device`` (mandatory) is the cli-mcp alias (``cl``, ``sa``, …) and
  is the **implicit top level** — every helper in this module derives
  the device folder from it; callers never pass the device as a
  bucket. This makes "show me everything for `cl`" a single
  ``listdir`` and gives each device a stable home for retention /
  pruning policies.
- ``bucket`` (optional, default ``None``) is a one-level sub-folder
  *under* the device root, used to group captures by purpose
  (``"nightly"`` for the scheduled job, ``"bug-1234-repro"`` for
  ad-hoc investigation captures, ``"save-for-later"``, …).
  ``bucket=None`` lands the file directly in the device root.

Bucket names are sanitised to ``[A-Za-z0-9._-]{1,60}``, may not
contain ``__`` (reserved as the filename separator), may not contain
``/`` (one level only — the device folder is the *first* level, the
bucket is the *second*), and must not match the canonical filename
shape (so a bucket can't be confused for a backup file).

Filename convention::

    <device>__<YYYYMMDD-HHMMSS>[__<desc>].md

- ``device`` prefix is **redundant with the folder** — but kept on
  purpose: ``restore_device`` uses the in-filename device prefix as a
  belt-and-suspenders safety check (refuses to apply ``cl__...md`` to
  ``device="sa"`` even if someone manually moved the file into
  ``BACKUP_DIR/sa/``).
- ``YYYYMMDD-HHMMSS`` is UTC, generated by the MCP at backup time.
- ``description`` is optional, sanitised to ``[A-Za-z0-9._-]{1,40}``.
  Missing description drops the trailing ``__<description>`` segment
  entirely.
- ``.md`` suffix is cosmetic — DNOS treats the filename as opaque, but the
  extension makes editors / IDEs render the saved config nicely.

DNOS ``save`` writes a plain text file with whatever name we hand it, and
``load override <name>`` accepts the same name verbatim, so the suffix
travels through both upload and restore unchanged.

Pre-2026-05-11 layout
---------------------

Earlier versions of this module used ``<BACKUP_DIR>/[<bucket>/]<file>``
where ``bucket`` defaulted to today's UTC date (``2026-05-10/`` etc.)
and the device alias appeared **only** in the filename. Existing files
in that shape are migrated to the new layout by
``scripts/scheduled/migrate-backups-layout.py``; nothing in this
module reads the old shape anymore.
"""

