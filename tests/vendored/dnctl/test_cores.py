def test_bt_keep_skips_cleanup(shell_calls):
    calls, _ = shell_calls
    r = cores_tool.get_core_backtrace(
        FULL_NAME, device="sa", confirm=True, keep=True,
    )
    assert r["kept"] is True
    assert r["workdir"].endswith(".qactl-bt-core-bgpd.cpid-199103.sig-6.2026-07-02.11-46-37")
    assert not any("rm -rf" in c["command"] for c in calls)


def test_bt_dry_run_without_confirm(shell_calls):
    calls, _ = shell_calls
    r = cores_tool.get_core_backtrace(FULL_NAME, device="sa")
    assert r["status"] == "ok"
    assert r["dry_run"] is True
    assert calls == []  # no device traffic
    assert any("gdb -batch" in c for c in r["commands"])
    assert any("rm -rf" in c for c in r["commands"])
    assert any("confirm" in w for w in r["warnings"])


def test_bt_unsupported_container(shell_calls):
    calls, _ = shell_calls
    r = cores_tool.get_core_backtrace(
        "frr/core-frr.cpid-1.sig-6.2026-07-02.11-46-37.tar",
        device="sa", confirm=True,
    )
    assert r["status"] == "error"
    assert "not supported yet" in r["errors"][0]
    assert "manual" in r["next_actions"][0].lower() or "routing_engine" in r["next_actions"][0]
    assert calls == []


@pytest.mark.parametrize("bad", [
    "",
    "core-bgpd.cpid-1.sig-6.tar",              # no container prefix
    "routing_engine/notacore.tar",              # not core-*
    "routing_engine/core-bgpd.tar.gz",          # wrong suffix
    "routing_engine/core-a;rm -rf /.tar",       # shell metacharacters
    "a/b/core-x.tar",                           # extra path component
])
def test_bt_rejects_bad_names(shell_calls, bad):
    calls, _ = shell_calls
    r = cores_tool.get_core_backtrace(bad, device="sa", confirm=True)
    assert r["status"] == "error"
    assert calls == []


def test_bt_missing_fullpath_errors_and_cleans_up(shell_calls):
    calls, outputs = shell_calls
    outputs["process.info"] = "tar: missing file\n"
    r = cores_tool.get_core_backtrace(FULL_NAME, device="sa", confirm=True)
    assert r["status"] == "error"
    assert "fullpath" in r["errors"][0]
    cmds = [c["command"] for c in calls]
    assert any("rm -rf" in c for c in cmds)  # workdir cleaned up
    assert not any("gdb" in c for c in cmds)  # never reached gdb


def test_bt_no_frames_is_error(shell_calls):
    calls, outputs = shell_calls
    outputs["gdb"] = "gdb: exploded\n"
    r = cores_tool.get_core_backtrace(FULL_NAME, device="sa", confirm=True)
    assert r["status"] == "error"
    assert r["backtrace"] == []
    assert any("no backtrace frames" in e for e in r["errors"])
    assert any("rm -rf" in c["command"] for c in calls)


