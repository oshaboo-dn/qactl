# --- `core list` parsing -----------------------------------------------------


def test_parse_core_list_rows():
    rows = cores_tool._parse_core_list(CORE_LIST_OUT)
    assert len(rows) == 3
    bgpd = rows[1]
    assert bgpd["type"] == "NCC"
    assert bgpd["id"] == "0"
    assert bgpd["file_name"] == FULL_NAME
    assert bgpd["size"] == "40M"
    assert bgpd["last_modified"].startswith("02-Jul-2026 14:47:18")
    assert bgpd["container"] == "routing_engine"
    assert bgpd["process"] == "bgpd"
    # thread-named core still parses; the *process* is just the tar's name.
    assert rows[0]["process"] == "QuaggaLogger"


def test_parse_core_list_merges_wrapped_file_names():
    # DNOS wraps an over-long file name onto a continuation row with every
    # other cell empty (verbatim from the live run, leftover workdir files).
    wrapped = """
| Type   | Id  | File name                                                                            | Size [Bytes]  | Last modified                                                        |
+--------+-----+--------------------------------------------------------------------------------------+---------------+----------------------------------------------------------------------+
| NCC    | 0   | routing_engine/core-bgpd.cpid-199103.sig-6.2026-07-02.11-46-37.tar                   | 40M           | 02-Jul-2026 14:47:18 UTC+03:00 (Israel)                              |
| NCC    | 0   | routing_engine/bt-sw279187/routing_engine/core-bgpd.cpid-199103.sig-6.2026-07-02.11- | 5.6M          | 02-Jul-2026 14:46:00 UTC+03:00 (Israel)                              |
|        |     | 46-37.lz4                                                                            |               |                                                                      |
"""
    rows = cores_tool._parse_core_list(wrapped)
    assert len(rows) == 2
    assert rows[1]["file_name"] == (
        "routing_engine/bt-sw279187/routing_engine/"
        "core-bgpd.cpid-199103.sig-6.2026-07-02.11-46-37.lz4"
    )
    assert rows[1]["size"] == "5.6M"
    # a non-bundle path derives no container/process.
    assert "container" not in rows[1]
    assert rows[0]["container"] == "routing_engine"


def test_parse_core_list_empty():
    assert cores_tool._parse_core_list("") == []
    assert cores_tool._parse_core_list("no cores here\n") == []


def test_list_cores_envelope(monkeypatch):
    captured = {}

    def _fake(tool, device, host, user, password, command, timeout, next_action):
        captured.update(tool=tool, device=device, command=command)
        return {"status": "ok", "stdout": CORE_LIST_OUT,
                "warnings": [], "errors": [], "next_actions": []}

    monkeypatch.setattr(cores_tool, "_run_on_device", _fake)
    r = cores_tool.list_cores(device="sa")
    assert captured["command"] == "show file core list"
    assert r["status"] == "ok"
    assert r["count"] == 3
    assert r["cores"][1]["file_name"] == FULL_NAME


def test_list_cores_error_passthrough(monkeypatch):
    monkeypatch.setattr(
        cores_tool, "_run_on_device",
        lambda *a, **k: {"status": "connect_error", "errors": ["boom"]},
    )
    r = cores_tool.list_cores(device="sa")
    assert r["status"] == "connect_error"
    assert "cores" not in r


# --- `core bt` ---------------------------------------------------------------


@pytest.fixture
def shell_calls(monkeypatch):
    """Stub run_linux_on_device with scripted step outputs; record commands."""
    calls = []
    outputs = {"process.info": PROCESS_INFO, "gdb": GDB_OUT, "assert": ASSERT_OUT}

    def _fake(tool, device, host, user, password, linux_command,
              timeout, next_action, *, shell_entry="run start shell"):
        calls.append({"command": linux_command, "timeout": timeout,
                      "shell_entry": shell_entry})
        if "tar -xf" in linux_command:
            stdout = outputs["process.info"]
        elif "gdb" in linux_command:
            stdout = outputs["gdb"]
        elif "grep -i assert" in linux_command:
            stdout = outputs["assert"]
        else:
            stdout = ""
        return {"status": "ok", "device": device, "host": host or "h",
                "command": linux_command, "stdout": stdout,
                "warnings": [], "errors": [], "next_actions": []}

    monkeypatch.setattr(cores_tool, "run_linux_on_device", _fake)
    calls_outputs = (calls, outputs)
    return calls_outputs


def test_bt_happy_path(shell_calls):
    calls, _ = shell_calls
    r = cores_tool.get_core_backtrace(FULL_NAME, device="sa", confirm=True)
    assert r["status"] == "ok"
    # binary comes from process.info fullpath, not the tar name.
    assert r["binary"] == "/usr/sbin/bgpd"
    assert r["signal"] == "SIGABRT"
    assert r["crashed_by"].startswith("/usr/sbin/bgpd -N")
    assert r["backtrace"][0].startswith("#0")
    assert any("bgp_open_capability" in f for f in r["backtrace"])
    # frames stop at the first "Thread N" header (crashed thread only).
    assert not any("libc.so.6" in f and "?? ()" in f for f in r["backtrace"])
    assert "bgp_open.c:1799" in r["assert_line"]

    cmds = [c["command"] for c in calls]
    assert len(cmds) == 4  # extract, gdb, assert-grep, cleanup
    wd = ("/core/core_dumps/containers/routing_engine/"
          ".qactl-bt-core-bgpd.cpid-199103.sig-6.2026-07-02.11-46-37")
    assert cmds[0].startswith(f"mkdir -p {wd} && tar -xf")
    assert "process.info.*" in cmds[0]
    assert "lz4 -d -f" in cmds[1]
    assert "set debuginfod enabled off" in cmds[1]
    assert "gdb -batch" in cmds[1] and "/usr/sbin/bgpd" in cmds[1]
    assert "thread apply all bt" not in cmds[1]
    assert "grep -i assert" in cmds[2]
    assert cmds[3] == f"rm -rf {wd}"
    # everything runs in the default shell (proven live: bgpd/gdb/lz4 all
    # visible from `run start shell` on the active NCC).
    assert all(c["shell_entry"] == "run start shell" for c in calls)
    # the decompress+gdb step gets the widened timeout floor.
    assert calls[1]["timeout"] >= 180


